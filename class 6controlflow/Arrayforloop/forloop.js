// for loop 
//initialization st: let i=1;
//condition st: i<=10;
//increment/decrement st: i=i+1;

let i=1;
for(i=1; i<=10; i++){
    console.log(i);
}

// for loop to print even numbers from 2 to 20
let j=2;
for(j=2; j<=20; j=j+2){
    console.log(j);
}


let z=100;
for(z=100; z>=50; z--){
    console.log(z);
}
